# Interpolar cadenas definidad y valores predefinidos
cadena = "tiene"
print("Kevin {} {} {}".format(cadena, 11, "años"))

# Interpolar valores definidos
print("Esta {cadena} {cadena2} de texto".format(cadena="es una", cadena2="cadena"))

# Con números
codigo = "código"
print("Hola {1} {1} {2}".format("escritores", "de", codigo))
print("Leo {0} de terror y {0} infantiles".format("cuentos"))