# Separa una cadena

# Sin Parámetros
mi_cadena = "Hola Mundo"; print(mi_cadena.split())

# 1 Parámetro
mi_cadena = "ajsjjdj#kajs#jSHh#jjdhd#eddff#"; print(mi_cadena.split("#"))

# 2 Parámetros
print(mi_cadena.split("#", 2))

# Se convierte en una lista
mi_cadena3 = mi_cadena.split("#", 2); print(len(mi_cadena3))